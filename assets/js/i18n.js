function applyLang(lang) {
  const dict = I18N[lang] || I18N.en;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const k = el.getAttribute("data-i18n");
    if (dict[k] != null) el.innerHTML = dict[k];
  });
  document.querySelectorAll("[data-i18n-attr]").forEach(el => {
    const [attr, k] = el.getAttribute("data-i18n-attr").split(":");
    if (dict[k] != null) el.setAttribute(attr, dict[k]);
  });
  document.querySelectorAll("[data-lang-label]").forEach(el => {
    el.textContent = lang === "ar" ? "EN" : "ع";
  });
  try { localStorage.setItem("soic-lang", lang); } catch (e) {}
}

function initLang() {
  let lang = "en";
  try { lang = localStorage.getItem("soic-lang") || "en"; } catch (e) {}
  applyLang(lang);
  document.querySelectorAll("[data-lang-toggle]").forEach(btn => {
    btn.addEventListener("click", () => {
      const cur = document.documentElement.lang === "ar" ? "ar" : "en";
      applyLang(cur === "ar" ? "en" : "ar");
    });
  });
}
